        <h2>What is Genshin Impact?</h2>
        <p>Genshin Impact is an open-world action role-playing game developed and published by miHoYo. It was released in September 2020 for various platforms, including PC, PlayStation, iOS, and Android. The game is set in the fantasy world of Teyvat, which is divided into seven distinct nations, each with its own culture, architecture, and elemental affinity.</p>
